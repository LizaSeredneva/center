#include <stdio.h> //Подключение библиотеки ввода/вывода
#include <math.h> //Математические функции
int main()
{
    int a=3;
    int b=4; //Объявление переменной
    int square;
    int perimeter;
    square = a * b;  //Арифметические операции
    printf("square: %d", square); //Вывод
    perimeter = 2 * (a + b); //Арифметические операции
    printf("perimeter: %d", perimeter); //Вывод
    return 0;
}
 
